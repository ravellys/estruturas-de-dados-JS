// tipos de variaveis

var num = 1; // declarando variavel inteira
num = 3; // atualizando variavel
var price = 1.5; // variavel de ponto flutuante
var myName = 'Packt'; // variavel string
var trueValue = true; // variavel bolleana
var nullVar = null; // valor null
var und; // variavel undefined

// vendo as variaveis
console.log('num: ' + num)
console.log('myName: ' + myName)
console.log('trueValue: ' + trueValue)
console.log('price: ' + price)
console.log('nullVar: ' + nullVar)
console.log('und: ' + und)
